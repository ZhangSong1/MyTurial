package com.design.patterns.commands;

public class Wings
{
  public void moveUp()
  {
    System.out.println("Plane moves upwards");
  }

  public void moveDown()
  {
    System.out.println("Plane moves downwards");
  }

  public void keepFlat()
  {
    System.out.println("Keep flat");
  }

  public void turnRight()
  {
    System.out.println("Turn right");
  }

  public void turnLeft()
  {
    System.out.println("Turn left");
  }

}
